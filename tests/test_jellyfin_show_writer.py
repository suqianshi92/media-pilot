def test_execute_show_write_copies_same_stem_subtitles(
    tmp_path: Path, monkeypatch,
) -> None:
    """每个 episode 的 same-stem 字幕被自动复制到 final 目录, 名称基于 target stem."""
    config = make_config(tmp_path)
    session_factory = create_session_factory(config)
    ep1_path = config.downloads_dir / "Show.S01E01.mkv"
    ep1_path.write_bytes(b"ep1")
    # 同源字幕 — 必须和视频 stem 完全一致
    (config.downloads_dir / "Show.S01E01.srt").write_text("sub1")
    # 另一个语言的同源字幕 (stem 以 video_stem 开头)
    (config.downloads_dir / "Show.S01E01.chs.srt").write_text("chs1")
    # 另一个 episode 的同源字幕 — 不应被本次 episode 携带
    (config.downloads_dir / "Show.S01E02.srt").write_text("other-ep")

    detail = make_show_detail()
    detail = MetadataDetail(**{
        **detail.__dict__,
        "images": MetadataImages(
            poster_url="https://img.test/poster.jpg",
            backdrop_url=None,
            logo_url=None,
        ),
    })
    client = make_http_client({
        "https://img.test/poster.jpg": httpx.Response(200, content=b"poster"),
    })

    with session_factory() as session:
        task = IngestTaskRepository(session).create(
            IngestTaskCreate(
                source_path=str(ep1_path),
                status="confirmed", current_step="confirmed",
                media_type="show",
            )
        )
        plan = build_show_write_plan(
            shows_dir=config.shows_dir,
            episodes=[
                EpisodeTarget(episode=1, season=1,
                              source_file=ep1_path, target_file=Path("")),
            ],
            detail=detail,
            task_id=task.id,
        )
        result = execute_show_write(
            session, task_id=task.id,
            detail=detail, plan=plan, client=client,
        )
        session.commit()

    # 状态 warning (backdrop/logo 缺失) 或 succeeded 都可; 关键是字幕存在
    assert result.status in ("succeeded", "warning")
    # E01 同源字幕全部复制到 final 目录
    final_dir = plan.final_target_dir
    target_stem = plan.show_dir_name + " - S01E01"
    assert (final_dir / f"{target_stem}.srt").exists()
    assert (final_dir / f"{target_stem}.chs.srt").exists()
    # 其它 episode 的字幕不在这里
    assert not any("E02" in p.name for p in final_dir.iterdir() if p.is_file())


