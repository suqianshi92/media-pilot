def test_execute_show_write_reports_progress_steps_in_order(tmp_path: Path) -> None:
    config = make_config(tmp_path)
    session_factory = create_session_factory(config)
    source_path = config.downloads_dir / "Show.Title.S01E01.1080p.mkv"
    source_path.write_bytes(b"episode-bytes")
    detail = make_show_detail()
    detail = MetadataDetail(**{
        **detail.__dict__,
        "images": MetadataImages(
            poster_url="https://img.test/poster.jpg",
            backdrop_url=None,
            logo_url=None,
        ),
    })
    episodes = [
        EpisodeTarget(episode=1, season=1, source_file=source_path, target_file=Path("")),
    ]
    client = make_http_client({
        "https://img.test/poster.jpg": httpx.Response(200, content=b"poster"),
    })
    progress_steps: list[str] = []

    with session_factory() as session:
        task = IngestTaskRepository(session).create(
            IngestTaskCreate(
                source_path=str(source_path),
                status="confirmed",
                current_step="confirmed",
                media_type="show",
            )
        )
        plan = build_show_write_plan(
            shows_dir=config.shows_dir,
            episodes=episodes,
            detail=detail,
            task_id=task.id,
        )

        result = execute_show_write(
            session,
            task_id=task.id,
            detail=detail,
            plan=plan,
            client=client,
            progress_callback=progress_steps.append,
        )
        session.commit()

    assert progress_steps[:3] == [
        "write_show_metadata_assets",
        "copy_episode_S01E01",
        "publish_show_to_library",
    ]
    assert result.status == "warning"  # backdrop/logo URL 为 None 会触发 warning


